    /** 靜態變數，儲存PatientHelper物件 */
    private static MedicineHelper mdh;

    /** 儲存JDBC資料庫連線 */
    private Connection conn = null;

    /** 儲存JDBC預準備之SQL指令 */
    private PreparedStatement pres = null;

    /**
     * 靜態方法<br>
     * 實作Singleton（單例模式），僅允許建立一個PatientHelper物件
     *
     * @return the helper 回傳PatientHelper物件
     */
